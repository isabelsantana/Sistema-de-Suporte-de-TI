class ClassificacaoChamado{
  
  private string software;
  private string hardware;
  private string nivelChamado;

  public string Software{
    get{
      return software;
    }
    set{
      software = value;
    }
  }

  public string Hardware{
    get{
      return hardware;
    }
    set{
      hardware = value;
    }
  }

  public string NivelChamado{
    get{
      return nivelChamado;
    }
    set{
      nivelChamado = value;
    }
  }

 

}