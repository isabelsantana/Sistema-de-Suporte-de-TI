class Cliente{

  private string endereco;
  private string telefone;
  private string nome;
  private int codigo;

  public string Endereco{
    get{
      return endereco;
    }
    set{
      endereco = value;
    }
  }

  public string Telefone{
    get{
      return telefone;
    }
    set{
      telefone = value;
    }
  }

  public string Nome{
    get{
      return nome;
    }
    set{
      nome = value;
    }
  }

  public int Codigo{
    get{
      return codigo;
    }
    set {
      codigo = value;
    }
  }
  
}